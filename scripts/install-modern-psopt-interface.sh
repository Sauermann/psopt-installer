# Download
psoptInstallerDownload modern-psopt-interface.zip https://github.com/Sauermann/modern-psopt-interface/archive/master.zip

# Compile
cd .packages
unzip ../.download/modern-psopt-interface.zip
cd modern-psopt-interface-master
